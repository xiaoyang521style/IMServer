package cn.appservice.po;

public class AddMeSetting {
    private int id;
    private int userId;
    private int open_phone;
    private int open_code;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getOpen_phone() {
        return open_phone;
    }

    public void setOpen_phone(int open_phone) {
        this.open_phone = open_phone;
    }

    public int getOpen_code() {
        return open_code;
    }

    public void setOpen_code(int open_code) {
        this.open_code = open_code;
    }
}
